class Bucket{
	String type;
	String color;
	double price;
	int quantity;
	boolean qualityIsGood;
	Bucket(String type,String color,double price,int quantity,boolean qualityIsGood){
		this.type=type;
		this.color=color;
		this.price=price;
		this.quantity=quantity;
		this.qualityIsGood=qualityIsGood;
	}
}