class ShoeLace{
	String laceName;
	String type;
	int length;
	String meterial;
	String color;
	float diameter;
	String style;
	boolean isElestic;
	float tensileStrength;
	
	ShoeLace(String laceName,
	String type,
	int length,
	String meterial,
	String color,
	float diameter,
	String style,
	boolean isElestic,
	float tensileStrength)
	{
		this.laceName=laceName;
		this.type=type;
		this.length=length;
		this.meterial=meterial;
		this.color=color;
		this.diameter=diameter;
		this.style=style;
		this.isElestic=isElestic;
		this.tensileStrength=tensileStrength;
	}
}