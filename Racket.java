class Racket{
	String type;
	String color;
	String name;
	String model;
	double price;
	boolean isDanger;
	String purpose;
	String place;
	String launcherName;
	double weight;
	
	Racket(String type,
	String color,
	String name,
	String model,
	double price,
	boolean isDanger,
	String purpose,
	String place,
	String launcherName,
	double weight){
		this.type=type;
		this.color=color;
		this.name=name;
		this.model=model;
		this.price=price;
		this.isDanger=isDanger;
		this.purpose=purpose;
		this.place=place;
		this.launcherName=launcherName;
		this.weight=weight;
		
	}
}